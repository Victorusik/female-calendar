async function getLLMAdvice(day) {
  // Заглушка для внешнего API LLM.
  // В реальном проекте здесь будет обращение к GigaChat/OpenAI и прокидывание system prompt:
  // "День цикла: {day}. Дай один короткий совет по самочувствию в одно предложение."

  const advices = [
    "Пейте больше воды и постарайтесь хорошо отдохнуть сегодня.",
    "Отличное время для легкой растяжки или йоги.",
    "Энергия на подъеме, идеальный момент для активной тренировки!",
    "Прислушивайтесь к сигналам тела и избегайте сильных стрессов.",
    "Можете позволить себе сладкое, если очень хочется, но знайте меру.",
    "Ваша продуктивность сегодня может быть очень высокой, используйте это!",
    "Хорошее время для короткой прогулки на свежем воздухе."
  ];

  const seed = day % advices.length;
  const pickedAdvice = advices[seed];

  return `День ${day}. ${pickedAdvice}`;
}

module.exports = { getLLMAdvice };
